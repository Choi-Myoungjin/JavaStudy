package jooMachine;

public class CStuff {
	String name;
	int pay;

	CStuff(String name, int pay) {
		this.name = name;
		this.pay = pay;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPay() {
		return pay;
	}

	public void setPay(int pay) {
		this.pay = pay;
	}
	
	

}
